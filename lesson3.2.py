# LOOPS

# .........................FOR LOOP

basket_of_fruits = ['mango','apple','banana','cherry','orange','kiwi']

for fruit in basket_of_fruits:
    print(fruit)
    
    
    
# .......................WHILE LOOP
# performs a set of instructions as long as the given condition is true

count = 1

while count <= 5:
    print(count)
    count += 1